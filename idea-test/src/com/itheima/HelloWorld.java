package com.itheima;
/*
* Java程序中最基本的组成单位是类
*  类的定义格式是
*       public class 类名{}
* */
public class HelloWorld {
    /*
    * 这是main方法
    * main方法是程序的入口方法，代码的执行是从main方法开始的
    * */
    public static void main(String[] args) {
        //这是输出语句，能够将“”里面的内容输出到控制台，并且“”里面的内容是可以修改的
        System.out.println("Hello World");
    }
}
